package eu.europa.ec.leos.web.support.event;

import com.google.common.eventbus.DeadEvent;
import com.google.common.eventbus.Subscribe;
import org.slf4j.Logger;
import org.slf4j.LoggerFactory;

public class DeadEventLogSubscriber {

    private static final Logger LOG = LoggerFactory.getLogger(DeadEventLogSubscriber.class);

    @Subscribe
    public void logEvent(DeadEvent event) {
        LOG.warn("Dead event: {}", event.getEvent());
    }
}
